class Calculator:
    """Class to run calculations such as:
       - addition
       - subsctraction
       - multiplication
       - division
    """

    def add(self, x, y):
        return x+y

    def subtract(self, x, y):
        return x-y

    def multiply(self, x, y):
        return x*y

    def divide(self, x, y):
        return x/y
